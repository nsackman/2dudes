				<p class="dream_text hide-mobile hide-tablet">We have one overarching mission: It is to launch successful products. Note the word successful. This is very important. Over and over again we watch beautiful edgy designs launch. And over and over again we see them ultimately fail. They do not fail because they are poorly designed; they fail because they were not designed for the correct purpose. We believe that a website designed for the correct purpose is more powerful than the most beautifully designed site, designed for the wrong purpose.</p>
